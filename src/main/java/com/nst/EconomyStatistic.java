    private boolean TimeChecker1(int day,int month,int year)             //kiểm tra xem thời gian nhập vào đã đúng định dạng chưa
    {
        boolean check = true;
        if(year % 4 ==0)
        {
            if(month == 2)
            {
                if(day >29) check=false;
            }
            else if(month == 4 || month == 6 || month == 9 || month == 11)
            {
                if(day>30) check = false;
            }
        }
        else
        {
           if(month == 2)
            {
                if(day >28) check=false;
            }
            else if(month == 4 || month == 6 || month == 9 || month == 11)
            {
                if(day>30) check = false;
            }           
        }
        return check;
    }
    private boolean TimeChecker2(int beginDay,int beginMonth,int beginYear,int endDay,int endMonth,int endYear)
    {           // kiểm tra xem thời gian bắt đầu có nhỏ hơn kết thúc không
        boolean check = true;
        if(beginYear> endYear)
        {
            check= false;
        }
        if(beginYear == endYear)
        {
            if(beginMonth>endMonth)
            {
                check= false;
            }
            if(beginMonth == endMonth)
            {
                if(beginDay> endDay)
                {
                    check= false;
                }
            }
        }
        return check;
    }
